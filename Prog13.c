//Ler 2 numeros inteiros e imprimi-los

#include<stdio.h>

void main(){
    int num1,num2;

    printf("Digite numero 1: ");
    scanf("%d",&num1);

    printf("Digite numero 2: ");
    scanf("%d",&num2);

    printf("Numero 1: %d\n",num1);
    printf("Numero 2: %d\n\n",num2);

    printf("Numero 1: %d\nNumero 2: %d\n",num1,num2);
}
